package chapter5;

public class ArrayInitialize {

  public static void main(String[] args) {
    int[] a = {1, 2, 3};
    int[] aa = new int[]{1, 2, 3};
    int[] aaa = new int[3];
    aaa[0] = 1;
    aaa[1] = 2;
    aaa[2] = 3;

    String[] b = {"hello", "world"};
    String[] bb = new String[]{"hello", "world"};
    String[] bbb = new String[2];
    bbb[0] = "hello";
    bbb[1] = "world";
  }
}
